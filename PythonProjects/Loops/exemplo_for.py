#RANGE é uma função que gera uma sequência de números, e sempre pula o ultimo número como exeplo, range(10) vai de 0 a 9, pulando o 10

#a variável contadora deverá assumir valores no intervalo entre 0 e 9
for x in range(10):
    #para cada um desses valores, vamos printar o valor da variável
    print(x)


#a variável contadora deverá assumir valores no intervalo entre 1 e 14, ou seja, iniciando em 1 e indo até 14
for x in range(1,15):
    #para cada um desses valores, vamos printar o valor da variável
    print(x)

#a variável contadora deverá assumir valores no intervalo entre 1 e 9 com incremento 2
for x in range(1,10,2):
    #para cada um desses valores, vamos printar o valor da variável
    print(x)